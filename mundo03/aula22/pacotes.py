#para criar um pacote, criamos uma pasta dentro do projeto, com o nome: pacote.uteis, dentro dessa pasta criaremos outras pastas com o nome dos assutos, cor, numeros e etc.., dentro de cada pasta temos que ter um arquivo __init__.py
