

cotacaoDolar = 4.99
valorEmReal = float(input('Quantos reais você tem? R$'))
resultado = valorEmReal / cotacaoDolar
print(f'Com R${valorEmReal} reais você pode comprar  US${resultado:.2f} dólares.')
#Neste código, eu adicionei :.2f na string de formatação para arredondar o resultado para duas casas decimais, que é comum em operações financeiras.










